--Реализовать на языке Haskell функцию, которая, чередуя элементы списков [a, b, ...] и [1, 2, ...], образует новый список [a, 1, b, 2, ...]

merge [] _ = []
merge (h1:t1) (h2:t2) = [(h1, h2)] ++ merge t1 t2

